let contador = 10;
console.log("pre-incremento:", ++contador);
// primero incrementa, luego usa el valor

contador = 10;
console.log("post-incremento:", contador++);
// primero usa el valor, luego incrementa

contador = 10;
console.log("pre-decremento:", --contador);
// primero decrementa, luego usa el valor

contador = 10;
console.log("post-decremento:", contador--);
// primero usa el valor, luego decrementa